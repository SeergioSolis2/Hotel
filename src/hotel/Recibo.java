        l = new ImageIcon(this.getClass().getResource("/Imagenes/FondoPisos.jpg")).getImage().getScaledInstance(FONDO.getWidth(),
        FONDO.getHeight(), Image.SCALE_SMOOTH);
        ImageIcon I = new ImageIcon(l);
        FONDO.setIcon(I);
        
          Image ii;
        ii = new ImageIcon(this.getClass().getResource("/Imagenes/Logo.png")).getImage().getScaledInstance(jLabel15.getWidth(),
        jLabel15.getHeight(), Image.SCALE_SMOOTH);
        ImageIcon II = new ImageIcon(ii);
        jLabel15.setIcon(II);
