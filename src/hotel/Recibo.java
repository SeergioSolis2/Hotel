    private void checkeo(){
        String cupo=this.Ocupantes.getText();
        String lim=this.limite.getText();
        int totalcupo=Integer.parseInt(cupo);
        int limites=Integer.parseInt(lim);
        int res=totalcupo-limites;
        System.out.println(res);
        if(res==1){
            this.jCheckBox1.isSelected();
            this.jCheckBox1.setSelected(true);
            this.jCheckBox2.setVisible(false);
        }else{
            this.jCheckBox2.isSelected();
            this.jCheckBox2.setSelected(true);
            this.jCheckBox1.setVisible(false);
        }
    }

